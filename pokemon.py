class Pokemon:
    def __init__(self, national_dex_number: int, name: str, family: str = None):
        self._national_dex_number = national_dex_number
        self._name = name
        self._family = family or name

    def __str__(self):
        return f"#{self._national_dex_number}: {self._name} ({self._family})"

    def get_national_dex_number(self) -> int:
        return self._national_dex_number

    def get_name(self) -> str:
        return self._name

    def get_family(self) -> str:
        return self._family


